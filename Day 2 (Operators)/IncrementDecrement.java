public class IncrementDecrement {
    public static void main(String args[]){
        int a = 10;
        int b = ++a; //++a means a=a+1 and it is pre increment
        System.out.println(a);
        System.out.println(b);

        int c = 20;
        int d = c++; //c++ means c=c+1 and it is post increment
        System.out.println(c);
        System.out.println(d);


        //increment is same as decrement, just use minus(-) instead of plus(+).
    }
}
